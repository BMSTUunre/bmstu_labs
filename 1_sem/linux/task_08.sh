#!/bin/bash

# Считываем значение A с клавиатуры
read A

# Проверяем, является ли A целым положительным числом
if [[ "$A" =~ ^[1-9][0-9]*$ ]]; then
    # Если A - положительное целое число, выводим квадрат
    echo $(( A * A ))
else
    # Если A не является положительным целым числом, выводим конкатенацию A + A
    echo "$A$A"
fi